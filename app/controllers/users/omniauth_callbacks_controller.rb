  skip_before_filter :verify_authenticity_token, only: :azureactivedirectory
  
  def azureactivedirectory

    auth = request.env["omniauth.auth"]

    @user = User.where(email: auth.info.email).first_or_create do |user|
      user.email = auth.info.email
      user.password = Devise.friendly_token[0,20]
      user.first_name = auth.info.first_name
      user.last_name = auth.info.last_name
      user.aad_uid = auth.info.uid
    end

    if @user.persisted?
      sign_in_and_redirect @user, :event => :authentication #this will throw if @user is not activated
      set_flash_message(:notice, :success, :kind => "Uniandes") if is_navigational_format?
    else
      redirect_to :root
    end

  end

